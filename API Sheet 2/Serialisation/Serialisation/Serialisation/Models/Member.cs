namespace Serialisation.Models
{
    /// <summary>
    /// Represents a member with a name and age.
    /// </summary>
    public class Member
    {
        /// <summary>
        /// Gets or sets the name of the member.
        /// </summary>
        public string Name { get; set; }

        /// <summary>
        /// Gets or sets the age of the member.
        /// </summary>
        public int Age { get; set; }
    }
}
